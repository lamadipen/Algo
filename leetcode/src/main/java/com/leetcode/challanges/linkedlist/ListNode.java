package com.leetcode.challanges.linkedlist;

/**
 * Definition for singly-linked list.
 */
public class ListNode {
    int val;
    com.leetcode.challanges.linkedlist.ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, com.leetcode.challanges.linkedlist.ListNode next) {
        this.val = val;
        this.next = next;
    }
}

